package hk220920;

public class Ex09 {
	public static void main(String[] args) {
		for(int i = 1; i<=9 ; i++) {
			System.out.println("5*"+i+"="+5*i);
		}
		System.out.println("====================");
		//---------------------------------------------
		int i = 1;
		while(i<=9) {
			System.out.println("5*"+i+"="+5*i);
			i++;
		}System.out.println("====================");
		//-----------------------------------------------
		for(i = 3; i<=7 ; i++) {
			System.out.println("3*"+i+"="+3*i);
		}
		System.out.println("====================");
		//----------------------------------------------
		i = 3;
		while(i<=7) {
			System.out.println("3*"+i+"="+3*i);
			i++;
		}
		System.out.println("====================");
	}
}
