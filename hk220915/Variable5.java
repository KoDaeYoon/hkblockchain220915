package hk220915;

public class Variable5 {
	public static void main(String[] args) {
		char ch = 97; //아스키 코드 값
		System.out.println(ch);
		char ch1 = 'b';
		System.out.println(ch1);
		char c = 'c';
		System.out.println(c);
		ch1 = 'A';
		System.out.println((int)ch1);
		int i = 65;
		System.out.println(i);
		System.out.println((char)i);
		ch1 = '가';
		System.out.println((int)ch1);
		
	}
}
